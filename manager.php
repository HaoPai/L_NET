                         <h3 id="progress">L网络建设过程</h3>
                         <p>L网络在2016年4月开始试运营，主要为部分用户提供互联网接入，资料分享和图书借阅等服务。2017年L网络服务器迁移到阿里云平台，完成相应备案手续，对整个网络进行了重新编码工作并正式对外上线，域名：<a href="http://www.l-net.cn">www.l-net.cn</a> , 当前主要提供图片分享和英语学习两项主要服务。</p>
                         <h3 id="photo">L网络图片分享服务</h3>
                         <p>随着智能手机设备的爆炸增长和使用，产生了巨量的图片内容，我们对于这些图片管理存在巨大困扰，如果出现手机遗失和硬盘损坏，将产生很大的损失。L网络图片分享致力于实现图片更加高效的管理和分享。L网络提供灵活的相册功能来方便组织图片，L网络提供原图存储，您们上传的图片不会有清晰度的损失。</p>
                         <h3 id="english">L网络英语学习服务</h3>
                         <p>学英语最为有效的方式就是背诵，而背诵最好的方法就是反复听原汁原味的英语句子，然后把句子默写下来，和原句子进行仔细对比。L网络选取了广受欢迎的新概念英语系列和一定的美剧，制作相应的声音和文本材料来帮助用户学习这些内容，最后提高英语水平。</p>
                         <p>L网络英语学习支持用户自主选择学习范围并组建自己的学习列表，通过不断听这些列表中的句子，再默写出来，后台程序将与原句子进行详细对比，发现并记录其中的错误。L网络也会跟踪你的学习进度，让你对自己的学习情况有清晰了解。L网络还提供单词记忆，单词联想等相关功能。</p>
